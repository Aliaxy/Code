int n, f[1002];

int main()
{
	n = read();
	f[0] = f[1] = 1;
	for (int i = 2; i <= n; i += 2)
	{
		f[i] = f[i - 1] + f[i / 2];
		f[i + 1] = f[i];
	}
	write(f[n]);

	return 0;
}