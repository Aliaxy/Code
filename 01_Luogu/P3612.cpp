std::string str;
long long n;

int main()
{
	std::cin >> str >> n;
	int len = str.size();
	while (len < n)
	{
		long long i = len;
		while (n > i) i <<= 1;
		i >>= 1;
		n -= i + 1;
		if (!n)
			n = i;
	}
	std::cout << str[n - 1];

	return 0;
}